for (let i = 1; i <= 10; i++) {
    switch (i) {
        case 1:
            console.log("1 - Pierwsza");
            break;
        case 2:
            console.log("2 - Druga");
            break;
        case 3:
            console.log("3 - Trzecia");
            break;
        case 4:
            console.log("4 - Czwarta");
            break;
        case 5:
            console.log("5 - Piąta");
            break;
        case 6:
            console.log("6 - Szósta");
            break;
        case 7:
            console.log("7 - Siódma");
            break;
        case 8:
            console.log("8 - Ósma");
            break;
        case 9:
            console.log("9 - Dziewiąta");
            break;
        case 10:
            console.log("10 - Dziesiąta");
            break;
        default:
            console.log(`${i} - Kolejna`);
    }
}
